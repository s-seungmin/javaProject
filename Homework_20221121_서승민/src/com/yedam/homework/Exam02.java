package com.yedam.homework;

public class Exam02 {
	public static void main(String[] args) {
		int x,y;
		x=37;
		y=91;
		
		System.out.printf("91 더하기 37 %d\n",y+x);
		System.out.printf("91 빼기 37 %d\n",y-x);
		System.out.printf("91 곱하기 37 %d\n",y*x);
		System.out.printf("91 나누기 37 %.2f\n",(double)y/x);
	}
}
